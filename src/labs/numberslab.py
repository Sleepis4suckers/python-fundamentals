def numeric_types(value1: int):

    print(bin(value1))
    print(oct(value1))
    print(hex(value1))
    print(value1)


# binary
def basic_binary():
    print(0b00011101)

# octal
def basic_octal():
    print(0o35)


# hexadecimal
def basic_hexadecimal():
    print(0x1d)


numeric_types(29)
